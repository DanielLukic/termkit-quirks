/// Escape character
// ignore: constant_identifier_names
const ESC = '\x1b';

/// Device Control String
// ignore: constant_identifier_names
const DCS = '${ESC}P';

/// Control Sequence Introducer
// ignore: constant_identifier_names
const CSI = '$ESC[';

/// Operating System Command
// ignore: constant_identifier_names
const OSC = '$ESC]';

/// String Terminator
// ignore: constant_identifier_names
const ST = '$ESC\\';

/// Bell (esc - \a)
// ignore: constant_identifier_names
const BEL = '\x07';

/// Backspace (esc - \b)
// ignore: constant_identifier_names
const BS = '\x08';

/// Horizontal Tab (esc - \t)
// ignore: constant_identifier_names
const HT = '\x09';

/// LineFeed (esc - \n)
// ignore: constant_identifier_names
const LF = '\x0A';

/// Vertical Tab (esc - \v)
// ignore: constant_identifier_names
const VT = '\x0B';

/// FormFeed (esc - \f)
// ignore: constant_identifier_names
const FF = '\x0C';

/// Carriage Return (esc - \r)
// ignore: constant_identifier_names
const CR = '\x0D';

/// Delete Character
// ignore: constant_identifier_names
const DEL = '\x7f';
